# ─── MULTI-THREAT SCENARIOS ───────────────────────────────────────────────────

class TestMultiThreatScenarios:
    def test_person_with_fire_raises_both_incidents(
        self, mock_model, alert_queue, tmp_path, monkeypatch
    ):
        """Arson scenario: person + fire in same frame → both threats flagged."""
        proc = _build_processor(mock_model, alert_queue, tmp_path, monkeypatch)
        img, _, gt = scene_person_with_fire()

        t0 = 1_000_000.0
        # Seed person at t0
        person_det = _make_det("person", gt[0]["bbox"], track_id=1)
        fire_det   = _make_det("fire",   gt[1]["bbox"], track_id=2)
        _run_scenario(proc, alert_queue, img, [person_det, fire_det], t=t0)
        # Fire → immediate. Person → not yet (< 30 s).
        incidents_0 = _drain_queue(alert_queue)
        types_0 = {i.threat_type for i in incidents_0}
        assert "fire" in types_0, "Fire should be flagged immediately"

        # 31 s later → person now loitering
        _run_scenario(proc, alert_queue, img, [person_det, fire_det], t=t0 + 31.0)
        incidents_1 = _drain_queue(alert_queue)
        types_1 = {i.threat_type for i in incidents_1}
        assert "loitering" in types_1, "Loitering should be flagged after 31 s"

    def test_multiple_loiterers_each_flagged(
        self, mock_model, alert_queue, tmp_path, monkeypatch
    ):
        """Two people loitering independently should each generate an incident."""
        proc = _build_processor(mock_model, alert_queue, tmp_path, monkeypatch)
        img, _, gt = scene_multiple_loiterers()

        t0 = 1_000_000.0
        dets = [_make_det("person", d["bbox"], track_id=i + 1) for i, d in enumerate(gt)]
        _run_scenario(proc, alert_queue, img, dets, t=t0)
        _drain_queue(alert_queue)   # discard seed

        _run_scenario(proc, alert_queue, img, dets, t=t0 + 31.0)
        incidents = _drain_queue(alert_queue)
        loitering = [i for i in incidents if i.threat_type == "loitering"]
        assert len(loitering) == 2, "Both loiterers should generate separate incidents"
        track_ids = {i.track_id for i in loitering}
        assert {1, 2} == track_ids

    def test_high_severity_threats_take_priority_actions(
        self, mock_model, alert_queue, tmp_path, monkeypatch, dispatcher
    ):
        """Critical threats (weapon + fire) should both generate full response actions."""
        proc = _build_processor(mock_model, alert_queue, tmp_path, monkeypatch)
        img = np.zeros((480, 640, 3), dtype=np.uint8)
        dets = [
            _make_det("weapon", [50, 50, 200, 400], track_id=1),
            _make_det("fire",   [300, 50, 600, 400], track_id=2),
        ]
        _run_scenario(proc, alert_queue, img, dets)

        for _ in range(alert_queue.qsize()):
            inc = alert_queue.get_nowait()
            dispatcher.dispatch(inc)
            if inc.threat_type == "weapon":
                assert "deter:lock_gates"        in inc.actions
                assert "notify:police"           in inc.actions
            elif inc.threat_type == "fire":
                assert "deter:sprinkler_trigger" in inc.actions
                assert "notify:fire"             in inc.actions


# ─── IMAGE QUALITY SANITY CHECK ───────────────────────────────────────────────

class TestSyntheticImageSanity:
    """Verify that generated test images are valid and non-trivial."""

    @pytest.mark.parametrize("scene_fn,expected_class", [
        (scene_loitering,          "person"),
        (scene_brawl,              "brawl"),
        (scene_fire,               "fire"),
        (scene_smoke,              "smoke"),
        (scene_abandoned_bag,      "abandoned_bag"),
        (scene_crowd_surge,        "crowd_surge"),
        (scene_weapon,             "weapon"),
        (scene_vehicle_intrusion,  "vehicle_intrusion"),
        (scene_graffiti,           "graffiti_vandalism"),
        (scene_suspicious_package, "suspicious_package"),
    ])
    def test_image_is_valid_and_annotated(self, scene_fn, expected_class):
        img, scene_name, detections = scene_fn()

        # Image shape
        assert img.shape == (480, 640, 3), f"{scene_name}: wrong image dimensions"
        assert img.dtype == np.uint8

        # Not a blank image (has some non-zero pixels)
        assert img.any(), f"{scene_name}: image is completely black"

        # Ground-truth annotations present
        assert len(detections) > 0, f"{scene_name}: no detections"
        assert detections[0]["class"] == expected_class, \
            f"{scene_name}: expected class '{expected_class}'"

        # Bounding boxes within image bounds
        for det in detections:
            x1, y1, x2, y2 = det["bbox"]
            assert 0 <= x1 < x2 <= 640, f"{scene_name}: invalid x bbox"
            assert 0 <= y1 < y2 <= 480, f"{scene_name}: invalid y bbox"
