# ─── HELPERS ──────────────────────────────────────────────────────────────────

def _build_processor(mock_model, alert_queue, tmp_path, monkeypatch):
    monkeypatch.chdir(tmp_path)
    return CameraProcessor("CAM-TEST", "synthetic", mock_model, alert_queue)


def _run_scenario(
    processor,
    alert_queue,
    img: np.ndarray,
    detections: list,
    t: float = 1_000_000.0,
):
    """Inject detections into the mock model and process the frame."""
    processor.model.track.return_value = make_results(detections)
    with patch("inference_engine.time.time", return_value=t):
        processor._process_frame(img, frame_idx=2)


def _drain_queue(q: queue.Queue):
    incidents = []
    while not q.empty():
        incidents.append(q.get_nowait())
    return incidents


def _make_det(class_name: str, bbox, track_id: int, conf: float = 0.90):
    return {
        "cls_id":   CLASS_IDS[class_name],
        "conf":     conf,
        "bbox":     bbox,
        "track_id": track_id,
    }


