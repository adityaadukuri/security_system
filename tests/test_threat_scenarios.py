class TestLoiteringScenario:
    # Use a realistic epoch base so the 60-second rate-limiter (which compares
    # against 0 for "never alerted") doesn't suppress the first incident.
    BASE_T = 1_000_000.0

    def test_loitering_detected_after_30s(
        self, mock_model, alert_queue, tmp_path, monkeypatch
    ):
        proc = _build_processor(mock_model, alert_queue, tmp_path, monkeypatch)
        img, _, gt = scene_loitering()
        bbox = gt[0]["bbox"]

        # Seed tracker at BASE_T
        _run_scenario(proc, alert_queue, img,
                      [_make_det("person", bbox, track_id=1)], t=self.BASE_T)
        assert alert_queue.empty(), "No loitering before 30 s"

        # Same position 31 s later
        _run_scenario(proc, alert_queue, img,
                      [_make_det("person", bbox, track_id=1)], t=self.BASE_T + 31.0)

        incidents = _drain_queue(alert_queue)
        assert any(i.threat_type == "loitering" for i in incidents), \
            "Loitering should be flagged after 31 s"

    def test_loitering_severity_is_medium(
        self, mock_model, alert_queue, tmp_path, monkeypatch
    ):
        proc = _build_processor(mock_model, alert_queue, tmp_path, monkeypatch)
        img, _, gt = scene_loitering()
        bbox = gt[0]["bbox"]

        _run_scenario(proc, alert_queue, img,
                      [_make_det("person", bbox, track_id=1)], t=self.BASE_T)
        _run_scenario(proc, alert_queue, img,
                      [_make_det("person", bbox, track_id=1)], t=self.BASE_T + 31.0)

        incidents = _drain_queue(alert_queue)
        loitering = [i for i in incidents if i.threat_type == "loitering"]
        assert loitering[0].severity == "medium"


